package zadanie2;

public class Czytelnia {
    private int cp, cc, pp, pc;
    public Czytelnia(int cp, int cc, int pp, int pc) {
        this.cp = cp;
        this.cc = cc;
        this.pp = pp;
        this.pc = pc;
    }
    public int getCp() {
        return cp;}
    public void setCp(int cp) {
        this.cp = cp;}
    public int getCc() {
        return cc;}
    public void setCc(int cc) {
        this.cc = cc;}
    public int getPp() {
        return pp;}
    public void setPp(int pp) {
        this.pp = pp;}
    public int getPc() {
        return pc;}
    public void setPc(int pc) {
        this.pc = pc;}
}